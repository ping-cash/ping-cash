#[derive(Debug)]
pub struct AuthorityRotated {
    pub pool: Pubkey,
    pub old_authority: Pubkey,
    pub new_authority: Pubkey,
}

#[error_code]
pub enum SwapError {
    #[msg("Pool is paused")]
    Paused,
    #[msg("Amount must be > 0")]
    ZeroAmount,
    #[msg("Output too small (below dust)")]
    OutputTooSmall,
    #[msg("Insufficient pool liquidity")]
    InsufficientLiquidity,
    #[msg("Spread BPS exceeds MAX_SPREAD_BPS")]
    SpreadTooHigh,
    #[msg("Arithmetic overflow")]
    MathOverflow,
    #[msg("vault.owner must equal the pool PDA")]
    WrongVaultOwner,
    #[msg("vault.mint must equal the corresponding mint passed to initialize_pool")]
    WrongVaultMint,
    #[msg("vault.close_authority must be None (vault must not be closeable)")]
    VaultMustNotBeCloseable,
    #[msg("Slippage exceeded — actual output below minimum_amount_out (sandwich-attack defense)")]
    SlippageExceeded,
    #[msg("New authority cannot be the zero pubkey")]
    ZeroPubkey,
    #[msg("Claimed authority is not the expected Squads multisig vault PDA (H-02)")]
    WrongAuthority,
    #[msg("AMM price deviates from Pyth oracle beyond MAX_ORACLE_DEVIATION_BPS — sandwich/manipulation defense")]
    PriceDeviatesFromOracle,
    #[msg("Pyth price account could not be parsed as a SolanaPriceAccount")]
    OracleAccountInvalid,
    #[msg("Pyth price is stale (>MAX_PYTH_STALENESS_SEC since last update) or non-positive")]
    OracleStaleOrInvalid,
}
