fn quote_usdc_to_ping(
    amount_in_usdc: u64,
    pool_usdc: u64,
    pool_ping: u64,
    spread_bps: u16,
) -> Result<u64> {
    let _ = pool_usdc;
    let gross = (amount_in_usdc as u128)
        .checked_mul(pool_ping as u128)
        .ok_or(SwapError::MathOverflow)?
        .checked_div((amount_in_usdc as u128).checked_add(pool_usdc as u128).ok_or(SwapError::MathOverflow)?)
        .ok_or(SwapError::MathOverflow)?;
    apply_spread(gross, spread_bps)
}

/// #62 H-04 rebuild start (ADR 0009 Pyth integration). Cross-checks the
/// AMM-derived price against a Pyth oracle reference and reverts if the
/// deviation exceeds MAX_ORACLE_DEVIATION_BPS. Caller wires in the Pyth
/// price account; on devnet this can be a Pyth mock (pyth-sdk-solana ships
/// fixture helpers). Production reads the canonical SOL/$PING/USDC feeds.
///
/// MAX_ORACLE_DEVIATION_BPS = 300 (3%) is intentionally wide for an AMM
/// pool that may also serve $PING-specific liquidity discontinuities.
/// Tighten to 100 (1%) post-launch once Raydium CLMM CPI lands and the
/// AMM curve more reliably tracks oracle.
///
/// Returns the AMM quote unchanged if oracle band check passes; reverts
/// with PriceDeviatesFromOracle otherwise. Pure validation — does not
/// override the AMM quote.

pub const MAX_ORACLE_DEVIATION_BPS: u16 = 300;


fn assert_amm_within_oracle_band(
    amm_usdc_per_ping_x64: u128,
    oracle_usdc_per_ping_x64: u128,
) -> Result<()> {
    // Compute |amm - oracle| / oracle in bps. Saturating math — at scaffold
    // start we accept clamping to u128::MAX rather than overflow-revert; the
    // band check below catches that case (any saturated diff > band → revert).
    let diff = if amm_usdc_per_ping_x64 > oracle_usdc_per_ping_x64 {
        amm_usdc_per_ping_x64 - oracle_usdc_per_ping_x64
    } else {
        oracle_usdc_per_ping_x64 - amm_usdc_per_ping_x64
    };
    let band = oracle_usdc_per_ping_x64
        .saturating_mul(MAX_ORACLE_DEVIATION_BPS as u128)
        .saturating_div(10_000);
    require!(diff <= band, SwapError::PriceDeviatesFromOracle);
    Ok(())
}

/// #62 H-04 helper: read $PING-per-USDC price from a Pyth price account
/// + staleness-check + confidence-interval-check + format as Q64.64 fixed
/// point matching assert_amm_within_oracle_band's expected scale.
///
/// On devnet/local this can be a Pyth mock fixture; production uses the
/// canonical $PING/USDC feed (TBD post-TGE — Pyth lists post-Raydium
/// liquidity onboarding).
///
/// Staleness threshold MAX_PYTH_STALENESS_SEC = 60 — if last price update
/// is older than 60s the swap reverts. Pyth typically updates every ~400ms
/// on mainnet so this is a generous tolerance.

pub const MAX_PYTH_STALENESS_SEC: i64 = 60;


fn read_pyth_ping_per_usdc_x64(
    price_account: &AccountInfo,
    clock: &Clock,
) -> Result<u128> {
    let price_feed = SolanaPriceAccount::account_info_to_feed(price_account)
        .map_err(|_| error!(SwapError::OracleAccountInvalid))?;
    let price = price_feed
        .get_price_no_older_than(clock.unix_timestamp, MAX_PYTH_STALENESS_SEC as u64)
        .ok_or(error!(SwapError::OracleStaleOrInvalid))?;
    // Reject if price is non-positive (Pyth permits negative for derivatives;
    // for a spot price feed we treat this as bad data).
    require!(price.price > 0, SwapError::OracleStaleOrInvalid);
    // Convert Pyth's i64 + expo into a Q64.64 fixed-point matching
    // assert_amm_within_oracle_band's amm_x64 scale. Pyth.expo is typically
    // negative (e.g., -8 means price is in units of 10^-8 USD per unit).
    let mantissa = price.price as u128;
    let q64_64_one: u128 = 1u128 << 64;
    // value_x64 = mantissa * 2^64 / 10^|expo|
    let abs_expo = price.expo.unsigned_abs() as u32;
    let scale = 10u128
        .checked_pow(abs_expo)
        .ok_or(SwapError::MathOverflow)?;
    mantissa
        .checked_mul(q64_64_one)
        .ok_or(SwapError::MathOverflow)?
        .checked_div(scale)
        .ok_or(SwapError::MathOverflow.into())
}

fn quote_ping_to_usdc(
    amount_in_ping: u64,
    pool_ping: u64,
    pool_usdc: u64,
    spread_bps: u16,
) -> Result<u64> {
    let _ = pool_ping;
    let gross = (amount_in_ping as u128)
        .checked_mul(pool_usdc as u128)
        .ok_or(SwapError::MathOverflow)?
        .checked_div((amount_in_ping as u128).checked_add(pool_ping as u128).ok_or(SwapError::MathOverflow)?)
        .ok_or(SwapError::MathOverflow)?;
    apply_spread(gross, spread_bps)
}

fn apply_spread(gross: u128, spread_bps: u16) -> Result<u64> {
    let net = gross
        .checked_mul((10_000 - spread_bps) as u128)
        .ok_or(SwapError::MathOverflow)?
        .checked_div(10_000)
        .ok_or(SwapError::MathOverflow)?;
    if net > u64::MAX as u128 { return err!(SwapError::MathOverflow); }
    Ok(net as u64)
}

/// #62 H-02 Squads V4 multisig CPI scaffold — autonomous source-side half
/// per shepherd coaching 2026-05-24. Constants + helper for verifying the
/// signer is the vault-PDA of a Squads multisig (not just any single key).
/// SDK fetch + real CPI calls land on a build host with cargo; the pattern
/// + program ID + PDA derivation are source-shippable now.
pub mod squads_multisig {
    use anchor_lang::prelude::*;

    /// Squads Protocol V4 program (mainnet). Pinned for build
    /// reproducibility under audit.
    pub fn id() -> Pubkey {
        // Mainnet Squads V4: SQDS4ep65T869zMMBKyuUq6aD6EgTu8psMjkvj52pCf
        Pubkey::try_from("SQDS4ep65T869zMMBKyuUq6aD6EgTu8psMjkvj52pCf")
            .expect("Squads V4 program ID is a known-valid base58")
    }

    /// Derive the canonical Squads V4 vault PDA for (multisig_pda,
    /// vault_index). Multisigs have N vaults — the protocol convention
    /// is vault_index = 0 for the primary treasury vault.
    ///
    /// PDA seeds: [b"multisig", multisig_pda.as_ref(), b"vault", &[vault_index]]
    ///
    /// Use this to assert a signer is in fact a multisig vault PDA (not
    /// a single dev key) before allowing privileged operations.
    pub fn vault_pda(multisig_pda: &Pubkey, vault_index: u8) -> (Pubkey, u8) {
        Pubkey::find_program_address(
            &[
                b"multisig",
                multisig_pda.as_ref(),
                b"vault",
                &[vault_index],
            ],
            &id(),
        )
    }

    /// Assert that `claimed_authority` is the Squads vault PDA derived
    /// from `multisig_pda` at `vault_index`. Returns Err(WrongAuthority)
    /// if not. Belt-and-braces alongside Anchor's Signer<'info> +
    /// `address = ...` constraints.
    ///
    /// Note: this does NOT verify the multisig itself has executed the
    /// underlying intent — that's enforced by the Squads program when
    /// the vault PDA signs (only Squads can sign as that PDA via CPI
    /// from a Squads transaction execution). The check here ensures
    /// the *pubkey shape* matches a multisig vault, blocking
    /// accidental single-key deploys.
    pub fn assert_is_vault_pda(
        claimed_authority: &Pubkey,
        multisig_pda: &Pubkey,
        vault_index: u8,
    ) -> Result<()> {
        let (expected, _bump) = vault_pda(multisig_pda, vault_index);
        require_keys_eq!(*claimed_authority, expected, crate::SwapError::WrongAuthority);
        Ok(())
    }
}

pub fn needs_hedge(pool_usdc: u64, pool_ping_usdc_value: u64) -> bool {
    if pool_usdc == 0 || pool_ping_usdc_value == 0 { return false; }
    let larger = pool_usdc.max(pool_ping_usdc_value) as u128;
    let smaller = pool_usdc.min(pool_ping_usdc_value) as u128;
    let drift_bps = (larger.checked_sub(smaller).unwrap_or(0))
        .checked_mul(10_000)
        .and_then(|x| x.checked_div(larger))
        .unwrap_or(0) as u16;
    drift_bps > HEDGE_THRESHOLD_BPS
}

