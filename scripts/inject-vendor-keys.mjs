#!/usr/bin/env node
/**
 * inject-vendor-keys.mjs — #50 helper for the OpenBao / ConfigMap vendor-key paste.
 *
 * Per ADR 0006 + CLAUDE.md §4.2 (IaC-FIRST): vendor keys flow through Git →
 * Flux → cluster, NOT direct kubectl patch (which would drift the manifest
 * vs source-of-truth in openova-private).
 *
 * This script does the LEANEST useful thing:
 *   - reads a KEY=VALUE env block from stdin (the founder's paste)
 *   - validates each line matches one of the 15 expected keys
 *   - normalises the values (strips whitespace, rejects empty, rejects
 *     placeholder values like "..." or "REPLACE_ME")
 *   - emits a YAML snippet ready to drop into
 *     openova-private:clusters/contabo-mkt/apps/ping/configmap.yaml
 *   - exits non-zero if any key is invalid or missing
 *
 * Workflow:
 *   1. founder copies the 15 keys from each provider dashboard (URLs in
 *      issue #50 latest comment) and pastes into a private channel
 *   2. claude runs: pbpaste | node scripts/inject-vendor-keys.mjs > snippet.yaml
 *   3. claude opens openova-private + edits configmap.yaml (paste snippet
 *      into the data: block) + commits + pushes
 *   4. Flux reconciles; pods auto-restart via SHA-pinned ConfigMap hash
 *
 * Usage:
 *   echo "TWILIO_ACCOUNT_SID=AC..." | node scripts/inject-vendor-keys.mjs
 *   pbpaste | node scripts/inject-vendor-keys.mjs > out.yaml
 *   cat keys.env | node scripts/inject-vendor-keys.mjs --check  # validate only, no output
 */

const EXPECTED_KEYS = [
  'TWILIO_ACCOUNT_SID',
  'TWILIO_AUTH_TOKEN',
  'TWILIO_VERIFY_SID',
  'PRIVY_APP_ID',
  'PRIVY_APP_SECRET',
  'TRANSFI_API_KEY',
  'TRANSFI_API_SECRET',
  'TRANSFI_WEBHOOK_SECRET',
  'CHAINALYSIS_API_KEY',
  'WHATSAPP_PHONE_NUMBER_ID',
  'WHATSAPP_ACCESS_TOKEN',
  'PERSONA_API_KEY',
  'PERSONA_WEBHOOK_SECRET',
  'ONFIDO_API_KEY',
  'ONFIDO_WEBHOOK_TOKEN',
];

// Optional but supported (the #58 Wise adapter ships against these)
const OPTIONAL_KEYS = [
  'WISE_API_KEY',
  'WISE_WEBHOOK_SECRET',
  'WISE_PROFILE_ID',
];

const PLACEHOLDER_VALUES = new Set([
  '',
  '...',
  '<paste>',
  'REPLACE_ME',
  'TODO',
  'TBD',
  'XXX',
  'changeme',
]);

const CHECK_ONLY = process.argv.includes('--check');

async function readStdin() {
  const chunks = [];
  for await (const chunk of process.stdin) chunks.push(chunk);
  return Buffer.concat(chunks).toString('utf8');
}

function parseEnvBlock(text) {
  const lines = text.split(/\r?\n/);
  const out = new Map();
  let lineNo = 0;
  for (const raw of lines) {
    lineNo += 1;
    const line = raw.trim();
    if (!line) continue;
    if (line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq <= 0) {
      throw new Error(`line ${lineNo}: invalid env syntax (no '=' or empty key)`);
    }
    const key = line.slice(0, eq).trim();
    let value = line.slice(eq + 1).trim();
    // strip surrounding quotes (single or double)
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    if (out.has(key)) {
      throw new Error(`line ${lineNo}: duplicate key ${key}`);
    }
    out.set(key, value);
  }
  return out;
}

function validateKeys(map) {
  const errors = [];
  const unknown = [];
  const allKnown = new Set([...EXPECTED_KEYS, ...OPTIONAL_KEYS]);

  for (const [k, v] of map.entries()) {
    if (!allKnown.has(k)) {
      unknown.push(k);
      continue;
    }
    if (PLACEHOLDER_VALUES.has(v)) {
      errors.push(`${k}: placeholder value '${v}' — paste the real value`);
    }
  }

  // Optional warning for missing-expected keys (we accept partial pastes since
  // a founder may roll out keys one provider at a time)
  const missing = EXPECTED_KEYS.filter(k => !map.has(k));
  return { errors, unknown, missing };
}

function emitYaml(map) {
  const sorted = [...map.entries()].sort(([a], [b]) => a.localeCompare(b));
  const lines = [
    '# Vendor-key block for openova-private:clusters/contabo-mkt/apps/ping/configmap.yaml',
    '# Generated by scripts/inject-vendor-keys.mjs at ' + new Date().toISOString(),
    '# Paste under the existing `data:` key of the ping-config ConfigMap.',
    '#',
    '# After pasting + committing + pushing to openova-private main, Flux',
    '# reconciles within ~30s and the SHA-pinned ConfigMap hash auto-rolls',
    '# the affected deployments (auth/offramp/compliance/notify/user).',
    '',
  ];
  for (const [k, v] of sorted) {
    // YAML quoting: keep simple — wrap in single quotes, escape single quotes
    const escaped = v.replaceAll("'", "''");
    lines.push(`  ${k}: '${escaped}'`);
  }
  return lines.join('\n') + '\n';
}

async function main() {
  const raw = await readStdin();
  if (!raw.trim()) {
    process.stderr.write(
      'usage:\n' +
        '  pbpaste | node scripts/inject-vendor-keys.mjs > snippet.yaml\n' +
        '  cat keys.env | node scripts/inject-vendor-keys.mjs --check\n'
    );
    process.exit(2);
  }
  let parsed;
  try {
    parsed = parseEnvBlock(raw);
  } catch (e) {
    process.stderr.write(`parse error: ${e.message}\n`);
    process.exit(1);
  }

  const { errors, unknown, missing } = validateKeys(parsed);

  if (unknown.length) {
    process.stderr.write(`warning: unknown keys ignored: ${unknown.join(', ')}\n`);
  }
  if (missing.length) {
    process.stderr.write(
      `info: missing ${missing.length}/${EXPECTED_KEYS.length} expected keys (partial-paste mode): ${missing.join(', ')}\n`
    );
  }
  if (errors.length) {
    process.stderr.write('errors:\n');
    for (const e of errors) process.stderr.write(`  - ${e}\n`);
    process.exit(1);
  }

  if (CHECK_ONLY) {
    process.stderr.write(
      `OK: ${parsed.size} key(s) validated (${parsed.size - unknown.length} known, ${unknown.length} unknown).\n`
    );
    return;
  }

  process.stdout.write(emitYaml(parsed));
}

main().catch(e => {
  process.stderr.write(`fatal: ${e.stack ?? e.message}\n`);
  process.exit(1);
});
