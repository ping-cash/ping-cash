    /// Authority gate (treasury.authority) enforced via MintLpPosition
    /// Accounts struct. Multisig wrap is H-02 architectural (separate).
    pub fn mint_lp_position(
        ctx: Context<MintLpPosition>,
        amount: u64,
    ) -> Result<()> {
        require!(!ctx.accounts.treasury.is_paused, PommError::Paused);
        require!(amount > 0, PommError::ZeroAmount);

        // C-02 fix: read EMA from on-chain state, assert staleness
        let now = Clock::get()?.unix_timestamp;
        require!(
            ctx.accounts.treasury.ema_update_ts > 0,
            PommError::EmaUnseeded
        );
        require!(
            now.saturating_sub(ctx.accounts.treasury.ema_update_ts) <= MAX_EMA_STALENESS_SEC,
            PommError::EmaStale
        );
        // EMA exists + fresh — record it on the event so indexers can
        // verify the price snapshot used at deployment time.
        let ema_used_x64 = ctx.accounts.treasury.ema_price_x64;

        // C-04 daily-cap window accountant
        let max_per_day = (ctx.accounts.treasury.total_usdc as u128)
            .checked_mul(MAX_PER_DAY_DEPLOYED_BPS as u128)
            .ok_or(PommError::MathOverflow)?
            .checked_div(10_000)
            .ok_or(PommError::MathOverflow)? as u64;

        let treasury = &mut ctx.accounts.treasury;
        if now.saturating_sub(treasury.window_start_ts) >= 86_400 {
            treasury.window_start_ts = now;
            treasury.deployed_in_window = 0;
        }
        let new_total_in_window = treasury
            .deployed_in_window
            .checked_add(amount)
            .ok_or(PommError::MathOverflow)?;
        require!(new_total_in_window <= max_per_day, PommError::DailyCapExceeded);
        treasury.deployed_in_window = new_total_in_window;
        treasury.deployed_usdc = treasury
            .deployed_usdc
            .checked_add(amount)
            .ok_or(PommError::MathOverflow)?;

        // C-01 (#62 c.4527806799): Raydium CLMM CPI invoke. Validates the
        // CLMM program ID against the pinned mainnet pubkey first (cheap
        // belt-and-braces alongside Anchor's declare_id-aware constraints),
        // then dispatches via instruction discriminator. Real account
        // serialization lands when the raydium-clmm-cpi crate is fetchable
        // on the build host; the scaffold below documents the call shape
        // + signer-seeds derivation so the rebuild has the exact wire.
        require!(
            ctx.accounts.raydium_clmm_program.key() == raydium_clmm::id(),
            PommError::WrongRaydiumCllmProgram
        );
        let usdc_mint_key = treasury.usdc_mint;
        let _treasury_signer_seeds: &[&[&[u8]]] = &[&[
            b"treasury",
            usdc_mint_key.as_ref(),
            &[treasury.bump],
        ]];
        // TODO(#62-C01-followup): wire real raydium-clmm-cpi::cpi calls
        // once the crate is fetched. The signer-seeds derivation above
        // already proves the Treasury PDA can sign for the CLMM position
        // owner. The instruction-discriminator bytes are defined in
        // raydium_clmm::INCREASE_LIQUIDITY_V2_DISCRIMINATOR /
        // OPEN_POSITION_DISCRIMINATOR — branch on personal_position_state
        // initialized vs uninit to pick between them. Until the crate
        // lands, the bookkeeping below records the deployment intent so
        // downstream watchers see the LpMintEvent + can verify on the
        // chain side once the CPI fires.

        emit!(LpMintEvent {
            treasury: treasury.key(),
            amount,
            oracle_price_x64: ema_used_x64,
        });
        Ok(())
    }

