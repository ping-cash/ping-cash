#[derive(Accounts)]
pub struct EmergencyWithdraw<'info> {
    /// Treasury authority (Squads multisig in prod). `address =` enforces
    /// signer matches the recorded authority; combined with the
    /// `is_paused` runtime check, this means only the multisig can
    /// drain — AND only after pausing first.
    #[account(address = treasury.authority)]
    pub authority: Signer<'info>,
    /// Per-mint Treasury PDA (H-02 c530322). mut for total_usdc decrement.
    #[account(mut, seeds = [b"treasury", usdc_mint.key().as_ref()], bump = treasury.bump)]
    pub treasury: Account<'info, Treasury>,
    /// USDC mint (read-only — decimals enforcement in transfer_checked).
    #[account(address = treasury.usdc_mint @ PommError::WrongUsdcMint)]
    pub usdc_mint: InterfaceAccount<'info, Mint>,
    /// Treasury's USDC reserve. address-pinned to the registered vault.
    #[account(mut, address = treasury.usdc_vault)]
    pub usdc_vault: InterfaceAccount<'info, TokenAccount>,
    /// H-04 fix (per #22 c.4527297108): emergency_withdraw destination
    /// MUST be owned by treasury.authority (Squads multisig in prod), not
    /// any caller-chosen wallet. Without this, a single-signer pause-and-
    /// drain attack works: pause treasury → emergency_withdraw to attacker
    /// ATA → unpause. ADR 0009 says "Multisig can only pause" — this
    /// constraint enforces that even an emergency exit can only route
    /// funds back to the authority's control.
    #[account(
        mut,
        constraint = destination_ata.owner == treasury.authority @ PommError::WrongEmergencyDestination,
        constraint = destination_ata.mint == usdc_mint.key() @ PommError::WrongVaultMint,
    )]
    pub destination_ata: InterfaceAccount<'info, TokenAccount>,
    pub token_program: Interface<'info, TokenInterface>,
}

impl<'info> EmergencyWithdraw<'info> {
    fn transfer_ctx(&self) -> CpiContext<'_, '_, '_, 'info, TransferChecked<'info>> {
        CpiContext::new(self.token_program.to_account_info(), TransferChecked {
            from: self.usdc_vault.to_account_info(),
            mint: self.usdc_mint.to_account_info(),
            to: self.destination_ata.to_account_info(),
            authority: self.treasury.to_account_info(),
        })
    }
}

