    pub fn propose_authority_rotation(
        ctx: Context<AdminTreasury>,
        new_authority: Pubkey,
    ) -> Result<()> {
        require!(new_authority != Pubkey::default(), PommError::ZeroPubkey);
        require!(
            new_authority != ctx.accounts.treasury.authority,
            PommError::SameAuthority
        );
        let now = Clock::get()?.unix_timestamp;
        let unlock_ts = now
            .checked_add(AUTHORITY_ROTATION_TIMELOCK_SEC)
            .ok_or(PommError::MathOverflow)?;
        ctx.accounts.treasury.pending_authority = new_authority;
        ctx.accounts.treasury.pending_authority_unlock_ts = unlock_ts;
        emit!(AuthorityRotationProposed {
            treasury: ctx.accounts.treasury.key(),
            current_authority: ctx.accounts.treasury.authority,
            proposed_authority: new_authority,
            unlock_ts,
            ts: now,
        });
        Ok(())
    }

    /// Step 2 — anyone may finalize after timelock; new authority pulls.
    pub fn finalize_authority_rotation(
        ctx: Context<FinalizeAuthorityRotation>,
    ) -> Result<()> {
        let now = Clock::get()?.unix_timestamp;
        require!(
            ctx.accounts.treasury.pending_authority != Pubkey::default(),
            PommError::NoPendingRotation
        );
        require!(
            now >= ctx.accounts.treasury.pending_authority_unlock_ts,
            PommError::TimelockNotElapsed
        );
        let old_authority = ctx.accounts.treasury.authority;
        let new_authority = ctx.accounts.treasury.pending_authority;
        ctx.accounts.treasury.authority = new_authority;
        ctx.accounts.treasury.pending_authority = Pubkey::default();
        ctx.accounts.treasury.pending_authority_unlock_ts = 0;
        emit!(AuthorityRotated {
            treasury: ctx.accounts.treasury.key(),
            old_authority,
            new_authority,
        });
        Ok(())
    }

    /// Cancel a pending rotation. Callable ONLY by the CURRENT authority.
    pub fn cancel_authority_rotation(
        ctx: Context<AdminTreasury>,
    ) -> Result<()> {
        require!(
            ctx.accounts.treasury.pending_authority != Pubkey::default(),
            PommError::NoPendingRotation
        );
        let cancelled = ctx.accounts.treasury.pending_authority;
        ctx.accounts.treasury.pending_authority = Pubkey::default();
        ctx.accounts.treasury.pending_authority_unlock_ts = 0;
        emit!(AuthorityRotationCancelled {
            treasury: ctx.accounts.treasury.key(),
            cancelled_authority: cancelled,
            ts: Clock::get()?.unix_timestamp,
        });
        Ok(())
    }

    /// C-03 step 2 (#62 c.4527806799): update the on-chain EMA accumulator
    /// from the live Pyth feed. Permissionless — anyone calling keeps the
    /// EMA fresh; no operator-trust hole because input is verifiable Pyth
    /// read, not caller-supplied.
    ///
    /// On first call (ema_update_ts == 0): seed ema_price_x64 from Pyth
    /// directly (no blending — blending against 0 would underweight first
    /// sample).
    ///
    /// On subsequent calls: blend per EMA_ALPHA_BPS:
    ///     ema_new = (alpha * pyth + (10000 - alpha) * ema_old) / 10000
    /// where alpha = 200 = 2%. With hourly cadence, ~50-tick half-life
    /// ≈ 7 days (ADR 0009 "7-day EMA" mandate).
    pub fn update_ema(ctx: Context<UpdateEma>) -> Result<()> {
        let now = Clock::get()?.unix_timestamp;
        let price_feed = SolanaPriceAccount::account_info_to_feed(
            &ctx.accounts.pyth_price_account,
        )
        .map_err(|_| error!(PommError::OracleAccountInvalid))?;
        let price = price_feed
            .get_price_no_older_than(now, MAX_PYTH_STALENESS_SEC as u64)
            .ok_or(error!(PommError::OracleStaleOrInvalid))?;
        require!(price.price > 0, PommError::OracleStaleOrInvalid);
        // Pre-audit CRIT (2026-05-27 sub-agent review on #22): Pyth
        // confidence-interval check. Reject if publishers disagree by more
        // than MAX_PYTH_CONF_BPS (1%) of the median price — same threshold
        // as internal-swap (cross-program consistency).
        let abs_price = price.price as u64;
        let conf_bps = (price.conf as u128)
            .checked_mul(10_000)
            .ok_or(PommError::MathOverflow)?
            .checked_div(abs_price as u128)
            .ok_or(PommError::MathOverflow)?;
        require!(
            conf_bps <= MAX_PYTH_CONF_BPS as u128,
            PommError::OracleConfidenceTooWide
        );
        // Convert Pyth i64 mantissa + expo to Q64.64
        let mantissa = price.price as u128;
        let q64_64_one: u128 = 1u128 << 64;
        let abs_expo = price.expo.unsigned_abs() as u32;
        let scale = 10u128
            .checked_pow(abs_expo)
            .ok_or(PommError::MathOverflow)?;
        let pyth_x64 = mantissa
            .checked_mul(q64_64_one)
            .ok_or(PommError::MathOverflow)?
            .checked_div(scale)
            .ok_or(PommError::MathOverflow)?;

        let treasury = &mut ctx.accounts.treasury;
        if treasury.ema_update_ts == 0 {
            // First sample — seed directly.
            treasury.ema_price_x64 = pyth_x64;
        } else {
            // EMA blend: alpha = EMA_ALPHA_BPS (200 = 2%)
            let alpha = EMA_ALPHA_BPS as u128;
            let one_minus_alpha = (10_000u128).saturating_sub(alpha);
            let new_weighted = pyth_x64
                .checked_mul(alpha)
                .ok_or(PommError::MathOverflow)?;
            let old_weighted = treasury
                .ema_price_x64
                .checked_mul(one_minus_alpha)
                .ok_or(PommError::MathOverflow)?;
            treasury.ema_price_x64 = new_weighted
                .checked_add(old_weighted)
                .ok_or(PommError::MathOverflow)?
                .checked_div(10_000)
                .ok_or(PommError::MathOverflow)?;
        }
        treasury.ema_update_ts = now;
        emit!(EmaUpdated {
            treasury: treasury.key(),
            pyth_price_x64: pyth_x64,
            ema_price_x64: treasury.ema_price_x64,
            update_ts: now,
        });
        Ok(())
    }

    pub fn emergency_withdraw(ctx: Context<EmergencyWithdraw>, amount: u64) -> Result<()> {
        require!(ctx.accounts.treasury.is_paused, PommError::EmergencyRequiresPause);

        // H-02 fix (per #22 c.4527297108): signer seeds bind to usdc_mint
        // matching the per-mint Treasury PDA. Same pattern as earn-vault C-04
        // (f7ea54f).
        let usdc_mint_key = ctx.accounts.treasury.usdc_mint;
        let seeds = &[
            b"treasury".as_ref(),
            usdc_mint_key.as_ref(),
            &[ctx.accounts.treasury.bump],
        ];
        let signer = &[&seeds[..]];
        token_interface::transfer_checked(
            ctx.accounts.transfer_ctx().with_signer(signer),
            amount,
            ctx.accounts.usdc_mint.decimals,
        )?;

        let treasury = &mut ctx.accounts.treasury;
        treasury.total_usdc = treasury.total_usdc.checked_sub(amount).ok_or(PommError::MathOverflow)?;
        emit!(EmergencyWithdrawEvent { treasury: treasury.key(), amount });
        Ok(())
    }
