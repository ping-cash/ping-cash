#[error_code]
pub enum PommError {
    #[msg("Treasury is paused")]
    Paused,
    #[msg("Amount must be > 0")]
    ZeroAmount,
    #[msg("Oracle price outside EMA band")]
    PriceOutOfBand,
    #[msg("Per-day deployment cap exceeded")]
    DailyCapExceeded,
    #[msg("Emergency withdraw requires paused state")]
    EmergencyRequiresPause,
    #[msg("Arithmetic overflow")]
    MathOverflow,
    #[msg("usdc_vault.owner must equal the treasury PDA")]
    WrongVaultOwner,
    #[msg("usdc_vault.mint must equal the usdc_mint passed to initialize_treasury")]
    WrongVaultMint,
    #[msg("usdc_vault.close_authority must be None (vault must not be closeable)")]
    VaultMustNotBeCloseable,
    #[msg("emergency_withdraw destination must be owned by treasury.authority (multisig)")]
    WrongEmergencyDestination,
    #[msg("New authority cannot be the zero pubkey")]
    ZeroPubkey,
    #[msg("collect_fees disabled in scaffold; full rebuild per ADR 0009 (real CLMM fee-read CPI) required first")]
    CollectFeesDisabledUntilRebuild,
    #[msg("mint_lp_position disabled in scaffold; full rebuild per ADR 0009 (Pyth oracle + on-chain EMA + Raydium CLMM CPI) required first")]
    DeployToClmmDisabledUntilRebuild,
    #[msg("Pyth price account could not be parsed as a SolanaPriceAccount")]
    OracleAccountInvalid,
    #[msg("Pyth price is stale (>MAX_PYTH_STALENESS_SEC since last update) or non-positive")]
    OracleStaleOrInvalid,
    #[msg("Pyth confidence interval exceeds MAX_PYTH_CONF_BPS — publishers disagree too widely")]
    OracleConfidenceTooWide,
    #[msg("Proposed authority must differ from current authority")]
    SameAuthority,
    #[msg("No pending authority rotation to finalize/cancel")]
    NoPendingRotation,
    #[msg("Authority rotation timelock (7 days) has not elapsed yet")]
    TimelockNotElapsed,
    #[msg("EMA accumulator has never been updated — call update_ema first")]
    EmaUnseeded,
    #[msg("EMA is stale (>MAX_EMA_STALENESS_SEC since last update) — call update_ema first")]
    EmaStale,
    #[msg("raydium_clmm_program account does not match the pinned mainnet CLMM program ID")]
    WrongRaydiumCllmProgram,
    #[msg("Claimed authority is not the expected Squads multisig vault PDA (H-02)")]
    WrongAuthority,
    #[msg("usdc_mint must equal treasury.usdc_mint — defense-in-depth on top of PDA seed binding (#22 CRIT #1)")]
    WrongUsdcMint,
}
