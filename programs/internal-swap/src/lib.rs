#[event]
#[derive(Debug)]
pub struct SwapEvent {
    pub pool: Pubkey,
    pub user: Pubkey,
    pub direction: SwapDirection,
    pub amount_in: u64,
    pub amount_out: u64,
}

#[event]
#[derive(Debug)]
pub struct SpreadChanged {
    pub pool: Pubkey,
    pub spread_bps: u16,
}

#[event]
#[derive(Debug)]
pub struct PausedEvent {
    pub pool: Pubkey,
    pub paused: bool,
    /// H-06 fix (#62 c.4527806799): which authority key triggered the
    /// state change. Pre-fix this event was unaccountable — auditor
    /// couldn't trace a specific multisig signer back to a pause action.
    pub caller: Pubkey,
    /// On-chain timestamp at the pause toggle. Pairs with caller for
    /// incident-response timeline reconstruction.
    pub ts: i64,
}

#[event]
#[derive(Debug)]
pub struct PoolInitialized {
    pub pool: Pubkey,
    pub authority: Pubkey,
    pub usdc_mint: Pubkey,
    pub ping_mint: Pubkey,
    pub usdc_vault: Pubkey,
    pub ping_vault: Pubkey,
    pub spread_bps: u16,
    pub bump: u8,
}

